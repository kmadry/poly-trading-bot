// Helper function to parse percentage string to decimal
const parsePercent = (str: string): number => {
  if (!str || str === '#DIV/0!' || str === '0,0%') return 0
  return parseFloat(str.replace(',', '.').replace('%', '')) / 100
}

// Helper function to parse currency string to number
const parseCurrency = (str: string): number => {
  if (!str || str === '#DIV/0!' || str === '$0,00') return 0
  return parseFloat(str.replace('$', '').replace(',', ''))
}

// Helper function to parse number with comma
const parseNumber = (str: string | number): number => {
  if (typeof str === 'number') return str
  if (!str || str === '#DIV/0!') return 0
  return parseFloat(str.toString().replace(',', '.').replace(' ', ''))
}

